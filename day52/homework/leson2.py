# ძირითადი კითხვები HTML-ზე
# რა არის HTML?
# HyperText Markup Language, ვებგვერდების სტრუქტურის განსაზღვრის ენა.

# რას ნიშნავს HTML-ის აბრევიატურა?
# HyperText Markup Language.

# რა არის ტეგი HTML-ში?
# ნიშნები, რომლებიც განსაზღვრავენ ელემენტებს ვებგვერდზე.

# როგორ გამოიყენება ელემენტები HTML-ში?
# ტეგების მეშვეობით, რომლებიც განსაზღვრავენ ელემენტის დასაწყისსა და დასასრულს.

# რა არის ატრიბუტი HTML ტეგში?
# ელემენტის დამატებითი მახასიათებლები, მაგალითად href ბმულებისათვის.

# რა არის HTML დოკუმენტის სტრუქტურა?
# <!DOCTYPE html>, <html>, <head>, <body>.

# რა არის <!DOCTYPE html> და რატომ არის მნიშვნელოვანი?
# HTML დოკუმენტის ტიპის განსაზღვრა, რომელიც ბრაუზერს ეუბნება რომელი ვერსიის HTML გამოიყენოს.

# როგორ გამოვიყენოთ <a> ტეგი ბმულებისათვის?
# <a href="URL">ტექსტი</a>.

# როგორ დავწეროთ სურათი HTML-ში?
# <img src="image.jpg" alt="აღწერა">.

# რა განსხვავებაა ბლოკურ და ხაზურ ელემენტებს შორის?
# ბლოკური ელემენტები მთელ სიგანეს იკავებენ, ხაზური მხოლოდ საჭირო სიგანეს.

# მოწინავე კითხვები HTML-ზე
# როგორ გამოვიყენოთ ფორმები HTML-ში?
# <form> ტეგების გამოყენებით.

# როგორ შევქმნათ ცხრილები HTML-ში?
# <table>, <tr>, <td> და <th> ტეგების გამოყენებით.

# რა არის <div> და <span> და როდის გამოიყენება ისინი?
# <div> ბლოკური ელემენტებისთვის, <span> ხაზური ელემენტებისთვის.

# როგორ ვასწოროთ ელემენტები გვერდზე CSS-ის გარეშე?
# HTML ატრიბუტებით, როგორიცაა align.

# რა არის სემანტიკური HTML?
# სტრუქტურული ტეგების გამოყენება, როგორიცაა <header>, <footer>, <article>.

# როგორ გამოვიყენოთ მედია ელემენტები (ვიდეო, აუდიო) HTML-ში?
# <video> და <audio> ტეგების გამოყენებით.

# რა არის HTML გლობალური ატრიბუტები?
# ატრიბუტები, რომლებსაც ყველა HTML ელემენტი იყენებს, მაგალითად id, class, style.

# როგორ დავამატოთ JavaScript HTML დოკუმენტში?
# <script> ტეგების გამოყენებით.

# როგორ გამოვიყენოთ HTML-ის ახალი თვისებები HTML5-ში?
# ახალი ტეგები, როგორიცაა <article>, <section>, <nav>.

# როგორ შევქმნათ ხელმისაწვდომი ვებგვერდები HTML-ის საშუალებით?
# <aria> ატრიბუტების და სემანტიკური HTML-ის გამოყენებით.

# პრაქტიკული კითხვები HTML პროექტებზე
# როგორ დავწეროთ ვებგვერდის ძირითადი სტრუქტურა HTML-ში?
# <!DOCTYPE html>, <html>, <head>, <body>.

# როგორ შევქმნათ ნავიგაცია საიტზე?
# <nav> და <a> ტეგების გამოყენებით.

# როგორ დავამატოთ ფავიკონი ვებგვერდზე?
# <link rel="icon" href="favicon.ico">.

# როგორ გამოვიყენოთ HTML ტეგები SEO-სთვის?
# სწორი სემანტიკური ტეგების გამოყენებით, როგორიცაა <h1>, <meta>.

# როგორ შევქმნათ რეზპონსიული დიზაინი HTML-ში?
# <meta name="viewport" content="width=device-width, initial-scale=1">.

# როგორ დავამატოთ გარე სტილები (CSS) HTML დოკუმენტში?
# <link rel="stylesheet" href="styles.css">.

# როგორ დავამატოთ გარე სკრიპტები (JavaScript) HTML დოკუმენტში?
# <script src="script.js"></script>.

# როგორ შევქმნათ კარუსელი სურათებისათვის HTML და CSS-ის გამოყენებით?
# სურათების <div> კონტეინერში და JavaScript-ით.

# როგორ დავამატოთ კონტაქტის ფორმა ვებგვერდზე?
# <form> ტეგებით, რომლებსაც აქვთ <input>, <textarea>, <button>.

# როგორ გავამზადოთ ვებგვერდი ჰოსტინგისთვის HTML დოკუმენტით?
# სწორი ფაილური სტრუქტურა, საჭირო ფაილების ატვირთვა სერვერზე.
# ქმნის ცარიელ სიას
my_list = []

# სიას ამატებს ელემენტებს 1-დან 7-მდე
my_list.extend([1, 2, 3, 4, 5, 6, 7])

# ცვლის ადგილებს ელემენტებს 2 და 3 ინდექსებზე (ანუ 3 და 4 მნიშვნელობებს)
my_list[2], my_list[3] = my_list[3], my_list[2]

# ამატებს ელემენტს 100 მე-4 ინდექსზე
my_list.insert(4, 100)

# შლის მე-5 ინდექსზე მყოფ ელემენტს
my_list.pop(5)

# შლის პირველ ელემენტს სიაში (ანუ 0 ინდექსზე მყოფს)
my_list.pop(0)

# ბეჭდავს საბოლოო სიას
print(my_list)